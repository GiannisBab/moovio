// Traffic intensity curve: 0.0–1.0 based on hour of day
// Peaks at 8–9 (morning rush) and 17–18 (evening rush), low at 2–5 (night)
function trafficIntensity(hour: number): number {
  const curve = [
    0.10, 0.08, 0.05, 0.04, 0.04, 0.06,  // 0–5
    0.15, 0.40, 0.85, 0.90, 0.65, 0.50,  // 6–11
    0.55, 0.60, 0.55, 0.60, 0.75, 0.95,  // 12–17
    0.80, 0.55, 0.35, 0.25, 0.18, 0.12,  // 18–23
  ]
  return curve[Math.min(23, Math.max(0, Math.round(hour)))]
}

// Deterministic pseudo-random number generator (mulberry32)
function seededRandom(seed: number) {
  let t = (seed + 0x6d2b79f5) | 0
  t = Math.imul(t ^ (t >>> 15), t | 1)
  t ^= t + Math.imul(t ^ (t >>> 7), t | 61)
  return ((t ^ (t >>> 14)) >>> 0) / 4294967296
}

